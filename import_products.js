async function main() {
  console.log("🚀 Starting import process...");

  // 1. Get or Create Agency safely
  let agency = await prisma.agency.findFirst({ where: { name: 'مارجن' } });
  if (!agency) {
    agency = await prisma.agency.create({ data: { name: 'مارجن' } });
    console.log("✔ Created agency 'مارجن'");
  }

  // 2. Process each product one by one to avoid transaction lock issues
  for (const x of productsList) {
    const productData = {
      name: x.n,
      factoryPrice: x.f,
      unitsPerCarton: x.q,
      retailPrice: x.r,
      wholesalePrice: x.w,
      agencyId: agency.id,
      unitFactoryPrice: x.f / x.q,
      unitWholesalePrice: x.w / x.q,
      unitRetailPrice: x.r / x.q,
    };

    try {
      await prisma.product.upsert({
        where: { barcode: x.b },
        update: productData,
        create: { ...productData, barcode: x.b }
      });
      console.log(`✅ Processed: ${x.n}`);
    } catch (err) {
      console.error(`❌ Failed to process ${x.n}:`, err.message);
    }
  }

  console.log("✔ Import finished.");
}

main()
  .catch(e => console.error(e))
  .finally(async () => await prisma.$disconnect());
